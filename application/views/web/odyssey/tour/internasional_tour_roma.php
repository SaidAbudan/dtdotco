                            <h2>Rincian Perjalanan Paket Honeymoon Maldives</h2><br><br>
                            <p>
                                Day 1, Wed: Singapore / Kuala Lumpur – Tashkent<br>
                                Flight from Singapore or Kuala Lumpur to Tashkent by Uzbekistan airways flight: From Kuala Lumpur, departure is at 18:35, from Singapore departure is at 22:05. Overnight flight. <br><br>

                                Day 2: Thu: Tashkent – Saint Petersburg <br>
                                Arrival to Tashkent international airport at 03:20 am local time from Singapore / Kuala Lumpur flight. Transit and flight for Saint Petersburg by Uzbek airways flight at 06:20. Arrival Saint Petersburg airport at 09:30 local time. Meet by your escort guide and transfer to the hotel. Check in and time for rest at the hotel. City tour in Saint Petersburg: During the tour, you will be taken out on the extra-comfortable bus, which will take you to the most precious monuments of the city’s rich history and original architecture carefully preserved as the world cultural heritage. During this panoramic city-tour of Saint Petersburg, you will be able to hear amazing stories and to take fine pictures of the main architectural and historical sites like Nevsky prospect, the Kazan Cathedral, the Winter Palace, Vasilievsky Island, St. Isaac Cathedral, Peter and Paul Fortress, the Senate Square and the Palace Square Lunch and dinners are at local restaurants. Overnight is at the hotel.<br><br>

                                Day 3, Fri: Saint Petersburg – Moscow (Sapsan train) <br>
                                Breakfast is at the hotel. City tour in Saint Petersburg: Hermitage Museum (main building). It is hard to imagine a trip to Saint Petersburg without visiting one of the most famous museums worldwide – the State Hermitage. Its priceless collection occupies five historical buildings of which the Winter Palace – the former residence of the Russian Royal family – is the most remarkable. Its rooms, decorated with marble and gold provide a fine background for the treasures of the Hermitage. Lunch will be at local restaurant. Kazan Cathedral - was built in 1811 and is usually associated with Russia’s victory over Napoleon in 1812. Transfer to railway station, board and departure for Moscow by fast train Sapsan. Upon arrival transfer for dinner at local restaurant, later on transfer to your hotel, check in and overnight is at the hotel. <br><br>

                                Day 4, Sat: Moscow <br>
                                reakfast at the hotel. City tour in Moscow: Moscow is the capital of the Russian Federation and represents a unique set of old architecture masterpieces and modern high-tech buildings. During the excursion, you will visit Red Square, the Kremlin, the Victory Park, the White House, the Arbat, Christ-the-Saviour Cathedral, the observation platform of Sparrow hills, Lomonosov University… and much more! It is an interesting and efficient introduction to Europe’s biggest city. Lunch and dinner will be at local halal restaurants. Overnight is at the hotel.<br><br>

                                Day 5, Sun: Moscow – Kazan (flight)<br>
                                Breakfast is at the hotel and late check out from the hotel. After lunch transfer to Moscow airport, registration and flight for Kazan. Arrival and dinner at local restaurant. Overnight is at the hotel. <br><br>

                                Day 6, Mon: Kazan<br>
                                Excursion "White-stone fortress". The Kazan Kremlin is the main attraction of the city, a UNESCO World Heritage Site. This is the official residence of the President of the Republic of Tatarstan and the State Museum-Reserve, which is visited annually by thousands of tourists. The white-stone Kremlin is the heart of an ancient city, embodying the culture of the west and east. Here the minarets of the main mosque of the city of Kul Sharifi rise, glistening with gold of the dome of the oldest Orthodox Annunciation Cathedral. Visit to Kul Sharifi. The sights of the millennial city have united the culture of the West and the traditions of the East. During the tour you will see the Old Tatar Sloboda, where the Tatar population, the fountain area, Lake Kaban, the stylized village "Tugan Avilym", the new Puppet Theater, Kazan University, Freedom Square - the cultural and administrative center of Kazan lived from the XVI century. Overnight.<br><br>

                                Day 7, Tue: Kazan – Tashkent (flight)<br>
                                After the breakfast transfer to Kazan airport. Flight to Tashkent by Uzbek airways flight (11:00-16:20). Arrival and check in to the hotel. Dinner is at local restaurant and overnight is at the hotel. <br><br>

                                Day 8, Wed: Tashkent – Urgench – Khiva (morning flight)<br>
                                In the morning you will have a flight to Urgench, then transfer to Khiva. Today you will have a wonderful sightseeing tour in the oldest city of Uzbekistan. It will be inside of Ichan-Qala, the inner city in Khiva rounded with strong walls. Centuries old sound buildings, palaces, mosques, Madrasahs, Mausoleums, Minarets, Caravan-Sarais and Bathhouses transformed Ichan-Qala into unique city-monument. Ichan-Qala consists of over 400 traditional dwelling houses and about 60 architectural monuments. In 1969 Ichan-Qala was designated a historical and architectural conservation area. The most remarkable sites in it are the Kunya-Ark fortress dating back to 17th century, found by Muhammad Arang Khan, Kalta Minor minaret and Tash Khouvli palace, an incredible sample of Khorezm architectural mastership of 1830s. Night is in a hotel. <br><br>

                                Day 9, Thu: Khiva – Bukhara (440 km, 7 hours)<br>
                                Early in the morning we will have transfer to a fascinating city of Bukhara.  With its more than 140 architectural monuments Bukhara is a "town museum" dating back to the Middle Ages. The city consists of narrow streets, green parks and gardens, historical and architectural monuments belong to different epochs, but locate very close to each other. The road from Khiva to Bukhara goes through famous Kizilkum desert. On the way we will enjoy the view to Amu Darya river, the deepest river in Central Asia, formed by the confluence of the Panj and Vakhsh in Tajikistan) and flowing into Aral See. Upon arrival in Bukhara you will have free time. Night is in a hotel. <br><br>

                                Day 10, Fri: Bukhara <br>
                                Breakfast is at the hotel. Full day city tour of Bukhara: Bahauddin Naqshbandi complex – located several km from Bukhara. Memorial complex of the famous Sufi Leader and Philosopher. Drive to the city and visiting Citadel Ark (4th b.c.) - Ark citadel is the most ancient citadel in Bukhara. Lunch will be at Adras traditional restaurant, following by dancing show program. Check in to the hotel and continuing the city tour: Shopping domes – Toki Saraffon, Toki Telpak Furushon & Toki Zargaron domes. Herbal tea degustation in Bukhara. Complex Lyabi-Khauz (14-17th c.) - the central sightseeing place of Bukhara city. Poi Kalon complex - the unique sample of architectural art of Bukhara. Dinner will be at national Uzbek house with master class of Bukhara pilaw preparing. Overnight is at the hotel.<br><br>

                                Day 11, Sat: Bukhara – Gijduvan - Samarkand (290 km, 4 hours)<br>
                                After the breakfast, we will start our way to Samarkand. On the way, we will stop over at Gijduvan village to visit famous private pottery workshop, which was previously visited by Mrs. Hillary Clinton, Sir Charles and many of other famous people. There we will have a chance to learn the history of pottery culture of Uzbekistan, short master class of pottery preparing and do buy the items you liked. Lunch will be at Pottery house, groups will have master class of Gijduvan shish kebabs, local bread preparing process. Afterwards continue drive to Samarkand. En route visiting Al Bukhary memorial complex. Upon arrival check in to the hotel. Dinner will be at local restaurant. Overnight is at the hotel. <br><br>

                                Day 12, Sun: Samarkand <br>
                                Breakfast is at the hotel. Full day city tour of Samarkand: Registan square – the complex of three historical building: Madrasah Ulugbek (15th c.), Madrasah Sher-dor (17th c.), Madrasah Tilla-Kori (17th c.) – the visiting card and the main attraction spot of Samarkand. Architectural complex Shakhi-Zindeh (11-15th c.) - the name Shah-I-Zinda (meaning "the living king") comes together with the legend about Kusam Ibn Abbas, the cousin of the prophet Muhammad who was buried here. Mausoleum Guri Emir - Tamerlane’s tomb (14-15th c.) - Gur-e Amir means "Tomb of the King". Lunch will be at New Arbat European restaurant. Dinner will be at traditional Uzbek house with cultural show program “Uzbek Bride”. Overnight is at the hotel.<br><br>

                                Day 13, Mon: Samarkand – Tashkent (350 km, 4,5 hours’ drive)<br>
                                Breakfast is at the hotel. Morning drive outside of the city to visit Al Bukhary memorial complex (12 km away of the city). Al Bukhary - Islamic scholar, who authored the hadith collection known as Sahih al-Bukhari, regarded by Sunni Muslims as one of the most authentic (Sahih) Hadith collections. He also wrote other books such as Al-Adab al-Mufrad. Returning to Samarkand and shopping at national bazaar Siab. Transfer to Tashkent (optional bullet train Afrosiab), upon arrival to Tashkent dinner at Milliy Taomlar national restaurant. Transfer to the hotel and check in. Overnight is at the hotel. <br><br>

                                Day 14, Tue: Tashkent <br>
                                City tour in Tashkent: Independence square (Red square in former USSR, Main Square of the country). Broadway - walking street between independence square and Amir Timur square. Amir Timur square – one of the central squares of Tashkent, erected on behalf of national hero – Amir Timur. Tashkent metro – among the most beautiful subways in the world. Lunch at Tashkent pilaw center – taste real wedding pilaw of Uzbekistan. Old city – old part of Tashkent city, the area, which preserves the traditional Uzbek style: Chorsu Bazaar, Kukaldash Madrasah (16th.c) and Hast Imam complex – the Islamic heart of the country. Muyi muborak Madrasah (16th c). Moreover, Madrasah exhibits one of the Islamic relics - the Othman Quran, the world’s oldest Quran copy dating back to 8th c. Dinner is at Bek restaurant following by dancing show program.<br><br>

                                Day 15, Wed: Tashkent – Flight <br>
                                Transfer to airport, flight for Kul or Sin at 07:45 am. Arrival to Kul at 18:30 and to Sin at 20:30 <br><br>

