            Hari ke-14 : Madrid – Barcelona  <br>
            Sarapan di hotel dan check-out. Hari ini kita akan meninggalkan kota Madrid menuju kota Barcelona dengan menggunakan kereta cepat (AVE). Setibanya di Barcelona kita akan langsung sightseeing mengenal kota ini, mengunjungi catedral Sagrada Familia, Plaza de España, Plaza de Catalunya, dan jalan Rambla. Kemudian kita akan melewati jalan Avenida Diagonal dan Passeig de Gracia dan parc Guell. Sore harinya kita akan mengunjungi stadion sepakbola FC Barcelona Camp Nou. Setelah makan malam kita akan menuju ke hotel untuk berisitirahat. (B/L/D) <br><br>

            Hari ke-15 : BARCELONA CITY TOUR – DOHA   <br>
