            Hari ke-6 : FES – CHEFCHAOEN – TANGIER  <br>
            Sarapan di hotel dan check-out. Pagi ini kita akan meninggalkan kota Fes menuju kota Chefchouen. Kota yang juga dikenal dengan kota biru, yang terkenal dengan keunikan dan kecantikannya. Setibanya di kota tersebut, kita akan sightseeing dan makan siang. Setelah makan siang, kita akan melanjutkan perjalanan ke kota Tangier.  Setibanya di kota Tangier, kita akan diajak menikmati kota Tangier terlebih dahulu. Menuju daerah tertinggi di kota tersebut untuk menikmati pemandangan selat Gibraltar. Makan Malam Chek In Hotel dan Istirahat. (B/L/D) <br><br>

            Hari ke-7 : TANGIER – TARIFA - GRANADA  <br>
            Hari ini kita akan meninggalkan negara Maroko menuju negara Spanyol dengan menggunakan kapal ferry. Melalui pelabuhan di kota Tanger kita akan tiba di kota Tarifa.  Setibanya di kota Tarifa, kita akan makan siang di Algeciras. Kemudian  kita akan melanjutkan Perjalanan menuju Granada . Setibanya di Granada Makan Malam dan Istirahat (B/L/D)<br><br>

            Hari ke-8 : GRANADA - CORDOBA <br>
            Sarapan di hotel check-out. kita akan diajak mengenal peninggalan kerajaan Islam terakhir di abad ke 14 yaitu Alhambra. Istana Alhambra dan Generelife merupakan paduan antara istana, taman dan juga kekuatan. Yang juga merupakan kota di dalam sebuah kota. Pada tahun 2015 Alhambra tercatat sebagai tempat bersejarah yang paling banyak dikunjungi di Spanyol. Setelah kunjungan selesai, kita akan makan siang dan melanjutkan tour kita mengunjungi Albayzin, kemudian kita akan melanjutkan perjalanan ke kota Córdoba. Setibanya disana Chek In Hotel dan Istirahat. (B/L/D)<br><br>

            Hari ke-9 : Córdoba – Seville  <br>
            Sarapan di hotel check-out Hari ini kita akan mengunjungi Mezquita-Catedral Córdoba, yang merupakan salah satu peninggalan Islam di Andalucia, sebuah mesjid yang dibangun di oleh Abd Al-Rahman I. Kita juga akan diajak menyusuri jalan jalan kecil disana, ke flower Street, Alcazar dan shalat dzuhur di musholla disana.  Selesai makan siang kita akan mengunjungi satu tempat bersejarah di Córdoba yakni Medinah Azahara. Kemudian melanjutkan perjalanan menuju kota Seville. Setibanya disana Makan Malam Chek In Hotel dan Istirahat. (B/L/D) <br><br>

            Hari ke-10 : SEVILLE – LISBON  <br>
            Pagi hari Chek Out Hotel. kita akan mengunjungi Plaza España, taman Maria Luisa lalu menyusuri jalan kecil disana mengunjungi La Giralda, Catedral de Sevilla, photostop di Torre del Oro dan juga Alcazar de Sevilla yang terkenal akan bangunannya yang menyerupai Alhambra. Setelah Makan siang kita akan menuju Lisbon ( +/- 6 jam ). Setibanya disana Makan Malam Chek In Hotel dan Istirahat. (B/L/D) <br><br>

            Hari ke-11 : LISBON CITY TOUR  <br>
            Hari ini kita akan City Tour Kota Lisbon.  kita akan shalat di Central Masjid of Lisbon dan mengunjungi Lisbon city Center. Photo stop di Belem Tower, Rossio Square, Jeronimos Monastery, Monument to the Discoveries serta berbelanja souvenir produk local. (B/L/D) <br><br>

            Hari ke-12 : LISBON-FATIMA –MADRID  <br>
            Acara tour hari ini di kita akan melanjutkan Perjalanan menuju kota Fatima dan diajak untuk mengunjungi/melewati: Basilica of Our Lady, Chapel of Apparitions, House of Francisco and Jacinta. kita akan mengunjungi Palace National de Sintra. Shalat di Central Masjid of Lisbon. Kemudian Bertolak Menuju Madrid dengan menggunakan Kereta. Kurang lebih Perjalanan selama 10 Jam.   Bermalam di Kereta.(B/L/D) <br><br>

            Hari ke-13 : MADRID CITY TOUR – TOLEDO – MADRID  <br>
