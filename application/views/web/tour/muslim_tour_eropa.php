                            <h2>Rincian Perjalanan Muslim Tour Eropa</h2>
                            <p style="text-align:justify;">
                                        Hari ke-1 :  JAKARTA - DOHA  <br>
                                        Sekitar pukul 18:00 diharapkan Jamaah sudah berada di Terminal 2 E (Lantai Atas belakang AW) Bandara SoekarnoHatta guna check-in Penerbangan ke Casablanca Via Doha Malam ini pukul 23.25.  <br><br>

                                        Hari ke-2 :  DOHA - MARAKECH KEDATANGAN <br>
                                        Hari ini kita akan terbang dari Doha menuju Marakech. Tiba di Marakech Sore Hari. Kita akan dijemput untuk Makan Malam . Kemudian Chek In Hotel dan Istirahat. (MOB/D) <br><br>

                                        Hari ke-3 : MARAKECH CITY TOUR  <br>
                                        Hari ini Setelah Sarapan Pagi kita akan mengunjungi Bahia Palace, Madrasa Benyoussef, Masjid Koutoubia dan juga Djeema El Fna square. Di Marrakech, kita akan diajak menikmati keindahan kota ini, berbelanja souvenir khas kota ini, dsb. Makan Malam, Chek In Hotel dan Istirahat. (B/L/D)<br><br>

                                        Hari ke-4 :  MARAKECH –  CASABLANCA - RABAT – MEKNES – FES <br>
                                        Sarapan Pagi dan Chek Out Hotel kita akan langsung Bertolak menuju Rabat. Sebelumnya kita akan mampir ke Casablanca untuk City Tour Singkat. Setibanya di Rabat banyak tempat yang dapat kita kunjungi seperti halnya Royal Palace (photostop), Hassan Tower dan Mohammed V Mausoleum. Di kota ini juga kita akan melewati Jalan Soekarno, yang merupakan tokoh Presiden pertama RI. Seusai makan siang kita akan melanjutkan perjalanan ke kota Meknes, sightseeing di ismaelite city ini. Kita akan berjalan jalan dan menikmati Kota Meknes ini. Kemudian selanjutnya kita akan melanjutkan perjalanan ke kota Fes. Setibanya di Fes kita akan Makan Malam, Chek In Hotel dan Istirahat. (B/L/D) <br><br>

                                        Hari ke-5 : FES CITY TOUR  <br>
                                        Sarapan Pagi DI Hotel. ini Hari ini kita akan mengunjungi mengunjungi  Bab Bou Jeloud, yang merupakan pintu gerbang kota serta mengunjungi Bou Inania Madrasah. kemudian Mengunjungi Madrasah ATTARID. Kita juga akan the Quaraouine mosque dan Universital Al Qarawiyin yang merupakan Universitas Pertama dan Tertua di Dunia.  Menikmati Keindahan Kota ini. Makan Malam di Local Restaurant, Kembali ke Hotel dan Istirahat.  (B/L/D) <br><br>

                                        Hari ke-6 : FES – CHEFCHAOEN – TANGIER  <br>
                                        Sarapan di hotel dan check-out. Pagi ini kita akan meninggalkan kota Fes menuju kota Chefchouen. Kota yang juga dikenal dengan kota biru, yang terkenal dengan keunikan dan kecantikannya. Setibanya di kota tersebut, kita akan sightseeing dan makan siang. Setelah makan siang, kita akan melanjutkan perjalanan ke kota Tangier.  Setibanya di kota Tangier, kita akan diajak menikmati kota Tangier terlebih dahulu. Menuju daerah tertinggi di kota tersebut untuk menikmati pemandangan selat Gibraltar. Makan Malam Chek In Hotel dan Istirahat. (B/L/D) <br><br>

                                        Hari ke-7 : TANGIER – TARIFA - GRANADA  <br>
                                        Hari ini kita akan meninggalkan negara Maroko menuju negara Spanyol dengan menggunakan kapal ferry. Melalui pelabuhan di kota Tanger kita akan tiba di kota Tarifa.  Setibanya di kota Tarifa, kita akan makan siang di Algeciras. Kemudian  kita akan melanjutkan Perjalanan menuju Granada . Setibanya di Granada Makan Malam dan Istirahat (B/L/D)<br><br>

                                        Hari ke-8 : GRANADA - CORDOBA <br>
                                        Sarapan di hotel check-out. kita akan diajak mengenal peninggalan kerajaan Islam terakhir di abad ke 14 yaitu Alhambra. Istana Alhambra dan Generelife merupakan paduan antara istana, taman dan juga kekuatan. Yang juga merupakan kota di dalam sebuah kota. Pada tahun 2015 Alhambra tercatat sebagai tempat bersejarah yang paling banyak dikunjungi di Spanyol. Setelah kunjungan selesai, kita akan makan siang dan melanjutkan tour kita mengunjungi Albayzin, kemudian kita akan melanjutkan perjalanan ke kota Córdoba. Setibanya disana Chek In Hotel dan Istirahat. (B/L/D)<br><br>

                                        Hari ke-9 : Córdoba – Seville  <br>
                                        Sarapan di hotel check-out Hari ini kita akan mengunjungi Mezquita-Catedral Córdoba, yang merupakan salah satu peninggalan Islam di Andalucia, sebuah mesjid yang dibangun di oleh Abd Al-Rahman I. Kita juga akan diajak menyusuri jalan jalan kecil disana, ke flower Street, Alcazar dan shalat dzuhur di musholla disana.  Selesai makan siang kita akan mengunjungi satu tempat bersejarah di Córdoba yakni Medinah Azahara. Kemudian melanjutkan perjalanan menuju kota Seville. Setibanya disana Makan Malam Chek In Hotel dan Istirahat. (B/L/D) <br><br>

                                        Hari ke-10 : SEVILLE – LISBON  <br>
                                        Pagi hari Chek Out Hotel. kita akan mengunjungi Plaza España, taman Maria Luisa lalu menyusuri jalan kecil disana mengunjungi La Giralda, Catedral de Sevilla, photostop di Torre del Oro dan juga Alcazar de Sevilla yang terkenal akan bangunannya yang menyerupai Alhambra. Setelah Makan siang kita akan menuju Lisbon ( +/- 6 jam ). Setibanya disana Makan Malam Chek In Hotel dan Istirahat. (B/L/D) <br><br>

                                        Hari ke-11 : LISBON CITY TOUR  <br>
                                        Hari ini kita akan City Tour Kota Lisbon.  kita akan shalat di Central Masjid of Lisbon dan mengunjungi Lisbon city Center. Photo stop di Belem Tower, Rossio Square, Jeronimos Monastery, Monument to the Discoveries serta berbelanja souvenir produk local. (B/L/D) <br><br>

                                        Hari ke-12 : LISBON-FATIMA –MADRID  <br>
                                        Acara tour hari ini di kita akan melanjutkan Perjalanan menuju kota Fatima dan diajak untuk mengunjungi/melewati: Basilica of Our Lady, Chapel of Apparitions, House of Francisco and Jacinta. kita akan mengunjungi Palace National de Sintra. Shalat di Central Masjid of Lisbon. Kemudian Bertolak Menuju Madrid dengan menggunakan Kereta. Kurang lebih Perjalanan selama 10 Jam.   Bermalam di Kereta.(B/L/D) <br><br>

                                        Hari ke-13 : MADRID CITY TOUR – TOLEDO – MADRID  <br>
                                        Hari ini tiba di Madrid. kita akan menikmati kota Madrid, full day tour di kota yang merupakan kota terbesar dan juga ibukota Spanyol.  Kita akan mengunjungi Royal Palace untuk berfoto, Puerta de Sol, Plaza Mayor, Plaza de España, jalan Gran Vía, Cibeles fountain, Puerta de Alcala dan monumen Colombus. Kita juga akan menikmati shopping areal di kota ini untuk dapat berbelanja. Setelah makan siang, kita akan mengunjungi mesjid di M30 yang merupakan mesjid terbesar di Spanyol. Tidak lupa pula kita akan mengunjungi stadiun sepakbola club ternama Real Madrid, Santiago Bernabeu dan berfoto di bullfight ring Plaza de Toros Las Ventas. Pada Sore hari kita akan singgah sebentar  di sebuah kota bersejarah yang dahulu pernah menjadi ibukota negara Spanyol yaitu kota Toledo. Pada abad ke-7, Islam pernah menduduki kota tersebut. Sampai saat ini masih banyak tempat bersejarah dan juga tradisi Islam yang melekat yang tertuang dalam hasil karya khas kota ini. Kembali ke Hotel, dan Istirahat. (B/L/D) <br><br>

                                        Hari ke-14 : Madrid – Barcelona  <br>
                                        Sarapan di hotel dan check-out. Hari ini kita akan meninggalkan kota Madrid menuju kota Barcelona dengan menggunakan kereta cepat (AVE). Setibanya di Barcelona kita akan langsung sightseeing mengenal kota ini, mengunjungi catedral Sagrada Familia, Plaza de España, Plaza de Catalunya, dan jalan Rambla. Kemudian kita akan melewati jalan Avenida Diagonal dan Passeig de Gracia dan parc Guell. Sore harinya kita akan mengunjungi stadion sepakbola FC Barcelona Camp Nou. Setelah makan malam kita akan menuju ke hotel untuk berisitirahat. (B/L/D) <br><br>

                                        Hari ke-15 : BARCELONA CITY TOUR – DOHA   <br>
                                        Sarapan di hotel dan check-out. Pagi ini kita akan sightseeing di kota Barcelona, photostop di Sagrada Familia, melihat view kota  inii dari Montjuic, Plaza de España dan stadium FCB Camp Nou. Setelah selesai sightseeing kita langsung bersiap menuju ke airport international Prat untuk kembali ke Jakarta. (B/L/D) <br><br>

                                        Hari ke-16 : JAKARTA KEDATANGAN   <br>
                                        In Sya Allah sore hari sekitar pukul 15:50 tiba di Jakarta. Selesailah perjalanan ini. Terima kasih keikut-sertaan Anda bersama kami, sampai jumpa di program Dreamtour berikutnya. (B/L/D) <br><br>
                                        
