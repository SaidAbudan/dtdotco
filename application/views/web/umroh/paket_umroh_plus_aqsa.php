                             Setelah sarapan pagi, persiapan ziarah kota Makkah. Ziarah mengunjungi Padang Arafah, Jabal Rahmah, Muzdalifah, Mina dan Jabal Tsur. Kemudian berakhir di Ja’ronah (bagi yang akan melaksanakan umroh kedua mengambil silahkan untuk miqot) bagi yang tidak mengikuti umrah kedua memperbanyak ibadah di Makkah. ( Makan Pagi, Makan Siang & Makan Malam )<br><br>

                             Day 07  : MAKKAH<br>
                             Memperbanyak ibadah di Masjidil Haram. (Free Program). ( Makan Pagi, Makan Siang & Makan Malam )<br><br>

                             Day 08 : MAKKAH-JEDDAH <br>
                             Setelah shalat Subuh dan Thawaf Wada’ persiapan untuk cek out hotel. Jamaah bertolak menuju Jeddah dan city tour, Mengunjungi Corniche, Masjid Qisas dan Juga Masjid Terapung, Selesai acara Jamaah Menuju ke Hotel untuk Beristirahat, ( Makan Pagi & Makan Siang )<br><br>

                             Day 09 : JEDDAH - AMMAN <br>
                             Setelah sarapan pagi di Hotel , check out Hotel dan perjalanan dilanjutkan menuju Airport Jeddah untuk Penerbangan Menuju Amman, Setibanya di Airport Amman, Perjalanan dilajutkan untuk Makan Siang di Local Restaurant, Kemudian kita akan mengunjungi Cave of the Seven Sleepers, dan King Abdullah Mosque. Kemudian kita akan makan malam dan melanjutkan perjalanan menuju Hotel untuk check in dan Istirahat. ( Makan Pagi, Makan Siang & Makan Malam )<br><br>

                             Day 10 : AMMAN – PETRA<br>
                             Setelah sarapan pagi di Hotel dan Check out dari Hotel, anda akan melakukan perjalanan menuju Petra. Anda akan melihat keindahan dari Lost City of Petra, Red Rose City of Petra dan anda akan menjelajahi Gua. Lalu anda akan berjalan melalu Canyon/Ngarai dan menikmati keindahan alam. Setelah itu anda akan diajak untuk melihat Nabetan City. Dilanjutkan dengan makan malam dan check in di Hotel. ( Makan Pagi, Makan Siang & Makan Malam )<br><br>

                             Day 11 : PETRA- - JERUSLEM - AQSA<br>
                             Setelah sarapan pagi di Hotel dan check out, hari ini akan melakukan perjalanan menjuju Jerusalem. Anda akan diajak untuk Mengunjungi Mesjid Al Aqsa , Dome of The Rock, Kota Tua Jerusalem, Maqam Salman Al Farisi , Mount of Olivers, Muslim Quarter , Mesjid Rabiaa Al Adawia . Makan siang dan makan malam di Local Resto. Kemudian check in Hotel dan Istirahat. ( Makan Pagi, Makan Siang & Makan Malam )<br><br>

                             Day 12 : JERUSALEM <br>
                             Setelah sarapan pagi di Hotel, hari ini anda akan diajak untuk mengunjungi Al Haram Al Ibrahimi, Dan tempat kelahiran Isa Alaih Assalam. Makan siang dan makan malam di Local Resto. ( Makan Pagi, Makan Siang & Makan Malam ) <br><br>

                             Day 13 : JERUSALEM - AMMAN <br>
                             Setelah sarapan pagi di Hotel, dan Check out Hotel anda akan diantar menuju Airport Amman untuk Penerbangan Menuju Jakarta. ( Makan Pagi & Makan Siang )<br><br>

