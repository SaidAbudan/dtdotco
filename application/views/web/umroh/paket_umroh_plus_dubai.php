                                        Day 10 : DUBAI (Jum'at, 13 Mei 2016)<br>
                                        Setelah makan pagi di Hotel, kita akan melakukan Dubai City Tour untuk mengunjungi berbagai tempat yaitu  Bastaki Area, Dubai Museum, Abra Taxi, Gold Souk dan Spice Souk. Kembali ke Hotel kemudian sore hari di jemput untuk Dinner di Dhow Cruise Creek. Setelah Dinner Kembali ke hotel dan beristirahat. (B,L,D)<br><br>

                                        Day 11 : DUBAI  - ABU DHABI (Sabtu, 14 Mei 2016)<br>
                                        Setelah makan pagi di Hotel, kita akan menuju Abu Dhabi untuk mengunjungi berbagai tempat. Kita akan melewati Jebel Ali Port, Capitol Gate Tower yang mirip dengan menara pisa dan Abu Dhabi Corniche, Grand Mosque dan photo stop outside Emirates palace, passing by Presidential palace, Heritage village. Setelah  makan siang dan mengunjungi Ferarri World untuk photo stop . Setelah itu kembali menuju Dubai dan makan malam di restoran setempat. Kembali ke hotel dan beristirahat. (B,L,D)<br><br>

                                        Day 12 : DUBAI – JAKARTA (Minggu, 15 Mei 2016)<br>
                                        Setelah makan pagi di Hotel, kita akan mengunjungi Dubai Mall dan acara bebas hingga diantar ke Airport Untuk penerbangan ke Jakarta pukul 18.40 pm waktu setempat.<br><br>

