                                    <p style="text-align:justify;">
                                        Hari 01 : JAKARTA – MADINAH <br>
                                        Di hari ini jamaah berkumpul di Bandara Soekarno – Hatta untuk penerbangan dari Jakarta  menuju Madinah Saudi dengan penerbangan  16 JAN : JKT - MED SV 825 1100 - 1710. Sesampainya di Madinah, jamaah akan kami antar menuju hotel untuk makan malam dan check in. (D) <br><br>

                                        Hari 02 : MADINAH <br>
                                        Setelah sarapan pagi,  jamaah akan berziarah ke Makam Rasullulah SAW & para sahabat dengan di damping Mutawif wanita dan laki – laki kemudian melaksanakan shalat sunah dan berdoa di Raudah. Setelah itu  Ziarah mengunjungi Jabal Uhud, Masjid Quba dan Pasar Kurma,  Lalu kembali ke Hotel untuk beristirahat. (B,L,D) <br><br>

                                        Hari 03 : MADINAH – MAKKAH <br>
                                        Mandi sunah ikhram dan persiapan untuk cek out. Setelah makan siang persiapan berangkat menuju ke Makkah dan mengambil Miqot di Biir Ali. Tiba di Makkah, langsung ke hotel Makkah untuk menyimpan barang-barang dan makan malam, kemudian melaksanakan ibadah Umroh. Setelah ibadah umroh selesai jamaah kembali ke hotel untuk beristirahat. (B,L,D)<br><br>

                                        Hari 04 : MAKKAH  <br>
                                        Setelah sarapan pagi, persiapan ziarah dikota Makkah. Ziarah mengunjungi Padang Arafah, Jabal Rahmah, Muzdalifah, Mina dan Jabal Tsur. Kemudian berakhir di Ja’ronah (bagi yang akan melaksanakan umroh kedua silahkan mengambil miqot) bagi yang tidak mengikuti umrah kedua memperbanyak ibadah dan shalat jum’at di Makkah. Setelah makan malam Thawaf Wadah  dan Istirahat (B,L,D) <br><br>

                                        Hari 05 : MAKKAH – JEDDAH – JEDDAH CITY TOUR<br>
                                        Memperbanyak Ibadah di Masjidil Haram. Tawaf Wada.  Kemudian Check  out Hotel dan  menuju ke Jeddah. Di Jeddah akan diajak untuk city Tour Jeddah dan makan malam dan kami antar kembali ke hotel untuk beristirahat dan persiapan besok pagi ke bandara (B,L,D) <br><br>

                                        Hari 06 : JEDAH-MADRID <br>
                                        Pagi ini anda akan berangkat ke Madrid dengan pesawat SV227 21 JAN JED MAD 0850 – 1310 menuju Madrid. Setibanya di Madrid jamaah akan mengunjungi Plaza Mayor, Puerta del Sol, Grand Via, Plaza del Toros, Royal Palace. Setelah itu kami antar menuju hotel untuk check-in dan beristirahat di hotel Madrid (B,L,D)<br><br>

                                        Hari 07 : MADRID <br>
                                        Perjalanan hari ini dimulai dengan city tour, dengan mengunjungi Cibeles Fountain, berbelanja di El Corte Ingles Castellena dan melihat pemandangan dari Cervantes Monument – Don Quixote dan Sancho Panza. Free time untuk berbelanja di Las Rozas Outlet dan Grand Via Street & Puerto Del Sol Square. (B,L,D) <br><br>

                                        Hari 08 : MADRID – PARIS <br>
                                        Setelah check out hotel, hari ini kita akan mengunjungi Santiago Bernabeu Stadium untuk berphoto, setelah itu jamaah akan kami antar Madrid Barajas Airport untuk melanjutkan perjalanan menuju Paris dengan flight detail : AF1601 23JAN MAD-CDG 1520-1725. Setelah itu kami akan antar menuju hotel Paris untuk check-in dan beristirahat (B,L,D) <br><br>

                                        Hari 09 : PARIS <br>
                                        Setelah tiba di Paris dihari ini jamaah akan mengikuti city tour dengan mengunjungi Eiffel Tower included access to 2nd floor (jika tidak tersedia di Menara Eiffel maka akan diganti dengan Menara Montparnasse), photo stop di Louvre Museum, setelah itu melihat pemandangan di Place de la Concorde, Champs Elysees, Arch du Triomphe, mengunjungi Fragonard Perfumery Museum, setelah itu waktu free untuk berbelanja di Galeries Lafayette. Selanjutnya akan kami antar kembali ke hotel untuk beristirahat. (B,L,D) <br><br>

                                        Hari 10 : PARIS - FRANKFURT <br>
                                        Setelah check-out hotel, dipagi hari jamaah akan berangkat menuju Frankfurt dengan perjalanan darat, kurang lebih 8 jam perjalanan. Setelah tiba di Frankfurt jamaah akan mengunjungi Kota Tua, Eschenheimer Turm, Goethe House dan Romerberg Square. Selanjutnya kami antar kembali menuju hotel Frankfurt untuk check-in dan beristirahat (B,L,D)<br><br>

                                        Hari 11 : FRANKFURT – COLOGNE - BRUSSELS<br>
                                        Setelah check-out hotel, acara hari ini kita  akan melanjutkan perjalanan menuju Cologne. Waktu tempuh kurang lebih 3 jam perjalanan menuju cologne, dan setelah tiba jamaah akan mengunjungi Grand Mosque Cologne dan Dom Cologne sebelum melanjutkan perjalanan menuju Brussels. Setelah tiba, jamaah akan kami antar menuju hotel untuk check-in dan beristirahat di hotel Brussels. (B/L/D)<br><br>

                                        Hari 12 : BRUSSELS – AMSTERDAM<br>
                                        Setelah check-out hotel, di hari ini jamaah akan mengikuti city tour di Brussels dengan mengunjungi Mannekin Piss, Atomium dan Grand Palace, setelah itu jamaah akan melanjutkan perjalanan menuju Amsterdam. Setelah tiba kami antar ke hotel di Amsterdam untuk check-in dan beristirahat.(B,L,D)<br><br>

                                        Hari 13 : AMSTERDAM <br>
                                        Dihari ini jamaah akan mengikuti Full Day Tour di Amsterdam dengan mengunjungi Zaanse Schans & Volendam, di sore harinya akan mengunjungi Diamond Factory (Include Cruise). Melihat pemandangan dari Dam square, Royal Palace, National Monument, Central Station, Rijksmuseum. Setelah itu jamaah akan kami antar menuju hotel Amsterdam untuk beristirahat. (B/L/D)<br><br>
                                        
                                        Hari 14 : AMSTERDAM – DEPARTURE <br>
                                        Dihari ini kroscek kembali barang bawaan sebelum melakukan check out hotel, free time sampai tiba saatnya jamaah kami antar menuju bandara untuk melanjutkan perjalanan pulang ke tanah air.<br><br>

                                        Hari 15 : JAKARTA <br>
                                        Dipagi ini jamaah akan tiba di Soekarno Hatta International Airport, dengan jadwal ketibaan pada pukul 1120 WIB.<br><br>
